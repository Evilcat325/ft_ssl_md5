#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*head;
	size_t	i;

	head = s1;
	while (*s1)
		s1++;
	i = 0;
	while (s2[i] && i < n)
	{
		*s1 = s2[i];
		s1++;
		i++;
	}
	*s1 = 0;
	return (head);
}
