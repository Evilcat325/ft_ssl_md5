#include "libft.h"

void	ft_putnbr(int n)
{
	long	nbr;
	int		d;

	if (n < 0)
		ft_putchar('-');
	nbr = n < 0 ? -(long)n : n;
	d = 1;
	while (nbr / d >= 10)
		d *= 10;
	while (d != 0)
	{
		ft_putchar((nbr / d) % 10 + '0');
		d /= 10;
	}
}
