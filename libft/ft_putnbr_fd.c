#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nbr;
	int		d;

	if (n < 0)
		ft_putchar_fd('-', fd);
	nbr = n < 0 ? -(long)n : n;
	d = 1;
	while (nbr / d >= 10)
		d *= 10;
	while (d != 0)
	{
		ft_putchar_fd((nbr / d) % 10 + '0', fd);
		d /= 10;
	}
}
